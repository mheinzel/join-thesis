-module(join_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    io:format("starting join application~n"),
    join_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    io:format("stopping join application~n"),
    ok.

